export enum EffectType {
	/** Sensory effects are subjective effects that directly alter a person's senses. These can include any combination of sight, sound, touch, taste, and smell. */
	sensory = 'sensory',
	/** Cognitive effects are subjective effects that directly alter or introduce new content to an element of a person's cognition. */
	cognitive = 'cognitive',
	/** Physical effects are subjective effects that directly affect part of a person's physical body. */
	physical = 'physical'
}
